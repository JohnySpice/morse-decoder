const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let str = "";
    for (let i = 0; i < expr.length; i += 2) {
        if (i % 10 === 0 && i !== 0) str += " ";

        if (expr[i] + expr[i + 1] === "10") {
            str += ".";
        }
        else if (expr[i] + expr[i + 1] === "11") {
            str += "-";
        }
    }
    return str.split(" ").map(e => {
        if (e === "") return " ";
        return MORSE_TABLE[e];
    }).join("");
}

module.exports = {
    decode
}